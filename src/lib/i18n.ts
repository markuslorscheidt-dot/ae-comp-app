/**
 * Holt einen verschachtelten Übersetzungswert
 */
export function getTranslation(lang: Language, path: string): string {
  const keys = path.split('.');
  let value: any = translations[lang];
  
  for (const key of keys) {
    if (value && typeof value === 'object' && key in value) {
      value = value[key];
    } else {
      // Fallback zu Deutsch
      value = translations['de'];
      for (const k of keys) {
        if (value && typeof value === 'object' && k in value) {
          value = value[k];
        } else {
          return path; // Gibt den Pfad zurück wenn nicht gefunden
        }
      }
      break;
    }
  }
  
  return typeof value === 'string' ? value : path;
}

/**
 * Ersetzt Platzhalter in Übersetzungen
 * z.B. "Hallo {name}" + { name: "Max" } => "Hallo Max"
 */
export function formatTranslation(text: string, params: Record<string, string | number>): string {
  return text.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? `{${key}}`));
}

/**
 * Holt die gespeicherte Sprache aus localStorage
 */
export function getSavedLanguage(): Language {
  if (typeof window === 'undefined') return 'de';
  const saved = localStorage.getItem('language');
  if (saved && (saved === 'de' || saved === 'en' || saved === 'ksh')) {
    return saved;
  }
  return 'de';
}

/**
 * Speichert die Sprache in localStorage
 */
export function saveLanguage(lang: Language): void {
  if (typeof window === 'undefined') return;
  localStorage.setItem('language', lang);
}
